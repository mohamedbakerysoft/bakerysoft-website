    private function baseCopy(string $name, string $description, string $metaDescription, string $cardSummary, array $content, array $faq): array
    {
        return [
            'description' => $description,
            'meta_title' => $name . ' | أدوات Calclyo العربية',
            'meta_description' => $metaDescription,
            'content' => array_merge(['card_summary' => $cardSummary], $content),
            'faq' => $faq,
        ];
    }

    private function applyHighValueOverride(string $name, array $copy): array
    {
        $override = $this->highValueToolOverrides()[ArabicSlug::make($name)] ?? null;

        if (! $override) {
            return $copy;
        }

        foreach ($override as $key => $value) {
            if ($key === 'content') {
                $copy['content'] = array_merge($copy['content'] ?? [], $value);
                continue;
            }

            $copy[$key] = $value;
        }

        return $copy;
    }


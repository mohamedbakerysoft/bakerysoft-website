    private function applyHighValueOverride(Conversion $conversion, array $copy): array
    {
        $key = $conversion->from_slug_ar . '__' . $conversion->to_slug_ar;
        $override = $this->highValueOverrides()[$key] ?? null;

        if (! $override) {
            return $copy;
        }

        foreach ($override as $field => $value) {
            if (is_array($value) && isset($copy[$field]) && is_array($copy[$field])) {
                $copy[$field] = $value;
                continue;
            }

            $copy[$field] = $value;
        }

        return $copy;
    }

